package com.kayluo.pokerface.dataModel;

public class TutorEntity {

    private String tutor_id;
    private String name;
    private String head_photo;
    private String signature;
    private String gender_eng;
    private String career;
    private String edu_level;
    private String university;
    private String total_class_time;
    private String rating;
    private String price;
    private String distance;


    public String getTutor_id() {
        return tutor_id;
    }

    public void setTutor_id(String tutor_id) {
        this.tutor_id = tutor_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHead_photo() {
        return head_photo;
    }

    public void setHead_photo(String head_photo) {
        this.head_photo = head_photo;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

    public String getGender_eng() {
        return gender_eng;
    }

    public void setGender_eng(String gender_eng) {
        this.gender_eng = gender_eng;
    }

    public String getCareer() {
        return career;
    }

    public void setCareer(String career) {
        this.career = career;
    }

    public String getEdu_level() {
        return edu_level;
    }

    public void setEdu_level(String edu_level) {
        this.edu_level = edu_level;
    }

    public String getTotal_class_time() {
        return total_class_time;
    }

    public void setTotal_class_time(String total_class_time) {
        this.total_class_time = total_class_time;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getUniversity() {
        return university;
    }

    public void setUniversity(String university) {
        this.university = university;
    }



}
